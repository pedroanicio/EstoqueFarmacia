    public void Listar() {
        try {
            PedidoDAO pedidoDAO = new PedidoDAO();
            List<Pedido> pedido = pedidoDAO.listarPedidos();

            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0);

            for (Pedido p : pedido) {
                model.addRow(new Object[]{
                    p.getIdPedido(),
                    p.getIdCliente(),
                    p.getIdMedicamento(),
                    p.getQtdeMedicamento(),
                    p.getDia()
                });
            }
        } catch (Exception e) {
            e.getMessage();
        }
    }


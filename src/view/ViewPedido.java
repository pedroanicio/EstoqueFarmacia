    private void jbAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAdicionarActionPerformed
        if (jtData.getText().isEmpty() | jtQtde.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira todos os dados");
        } else {
            String dia = jtData.getText();
            int qtde = Integer.parseInt(jtQtde.getText());

            Pedido pedido = new Pedido();
            Cliente c = new Cliente();
            Medicamento m = new Medicamento();

            pedido.setDia(dia);
            pedido.setQtdeMedicamento(qtde);
            //combo box
            c = (Cliente) jcCliente.getSelectedItem();
            m = (Medicamento) jcMedicamento.getSelectedItem();

            pedido.setIdCliente(c);
            pedido.setIdMedicamento(m);

            PedidoDAO pedidoDAO = new PedidoDAO();
            pedidoDAO.inserir(pedido);

            jtData.setText("");
            jtQtde.setText("");
            jcCliente.setSelectedIndex(0);
            jcMedicamento.setSelectedIndex(0);
        }

        Listar();
    }//GEN-LAST:event_jbAdicionarActionPerformed


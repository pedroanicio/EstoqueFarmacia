        String rua = jtRua.getText();
        int numero = Integer.parseInt(jtNumero.getText());

        Cliente cliente = new Cliente();
        cliente.setNome(nome);
        cliente.setTelefone(telefone);
        cliente.setCpf(cpf);
        cliente.setBairro(bairro);

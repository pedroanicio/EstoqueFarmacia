    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        try {
            Cliente cliente = new Cliente();

            cliente.setIdCliente(Integer.parseInt(jtIDcliente.getText()));

            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.excluir(cliente);

            JOptionPane.showMessageDialog(null, "Dados excluidos com sucesso.");
            Listar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir os dados.");
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed

        String nome = jtNome.getText();
        String telefone = jtTelefone.getText();
        String cpf = jtCpf.getText();
        String bairro = jtBairro.getText();
        String rua = jtRua.getText();
        int numero = Integer.parseInt(jtNumero.getText());
        int id = Integer.parseInt(jtIDcliente.getText());

        try {
            Cliente cliente = new Cliente();

            cliente.setNome(nome);
            cliente.setTelefone(telefone);
            cliente.setCpf(cpf);
            cliente.setBairro(bairro);
            cliente.setRua(rua);
            cliente.setNumero(numero);
            cliente.setIdCliente(id);

            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.editar(cliente);

            JOptionPane.showMessageDialog(null, "Dados editados com sucesso.");
            Listar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao editar os dados." + e);
        }
    }//GEN-LAST:event_jbEditarActionPerformed

